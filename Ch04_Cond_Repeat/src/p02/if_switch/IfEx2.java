package p02.if_switch;

public class IfEx2 {

	public static void main(String[] args) {
		int num =74;
		if(num<10) 
			System.out.println("num의 값은 10미만");
		else if(num<100)
			System.out.println("num의 값은 10~100미만");
		else if(num<1000)
			System.out.println("num의 값은 100~1000미만");
		else
			System.out.println("num의 값은 1000이상");
		
		
			
	}

}
